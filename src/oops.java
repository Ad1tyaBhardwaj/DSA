public class oops {
    public static void addNums(int a, int b){
        System.out.println(a+b);
    }
    public static double addNums(double a, double b){
        return a+b;
    }

    public static void main(String[] args) {
        addNums(2,3);
        System.out.println(addNums(10.0,8.6));

    }

}
